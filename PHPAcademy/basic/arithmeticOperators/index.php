<?php
	$num1 = 10;
	$num2 = 2;
	
	$result = $num1 + $num2;
	echo "<br> $result";
	
	
	$result = $num1 - $num2;
	echo "<br> $result";
	
	$result = $num1 * $num2;
	echo "<br> $result";
	
	$result = $num1 / $num2;
	echo "<br> $result";
	
	// modulo 
	$result = $num1 % $num2;
	echo "<br> $result";
	
	// incremento
	$result = $num1++;
	echo "<br> $result";
	
	// decremento
	$result = $num1--;
	echo "<br> $result";
?>